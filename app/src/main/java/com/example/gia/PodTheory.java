package com.example.gia;

public class PodTheory {
    private String id;
    private String name;


    private String id_te;

    public PodTheory() {
        this.id = id;
        this.name = name;
        this.id_te = id_te;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId_te() {
        return id_te;
    }

    public void setId_te(String id_te) {
        this.id_te = id_te;
    }
}
